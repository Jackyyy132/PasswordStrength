    private void SavePasswordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SavePasswordButtonActionPerformed
        if (GenPassTB.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "There are no passwords to save", "Password Save Error", JOptionPane.INFORMATION_MESSAGE);
        }
        
        else{
            Passwords.add(GenPassTB.getText());
            
        try {
            BufferedWriter out = new BufferedWriter(new FileWriter("SavedPasswords.txt", true));
            for (int i = 0; i < Passwords.size(); i++){
                out.write(Passwords.get(i) + "\r\n");
            }
            
            out.close();
            JOptionPane.showMessageDialog(rootPane, "Your password has been saved successfully." , "Password Saved Success", JOptionPane.INFORMATION_MESSAGE);
        }
        catch (IOException ex) {
            Logger.getLogger(Password.class.getName()).log(Level.SEVERE, null, ex);

        }
        
        }

    }//GEN-LAST:event_SavePasswordButtonActionPerformed

