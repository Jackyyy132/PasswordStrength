    private void GenPassButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GenPassButtonActionPerformed
        Random r = new Random();
        char RandomLetter;
        String RandomPassword = "";
        String Capital = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
        String Lower = "abcdefghijklmnopqrstuvwxyz";
        String Numbers = "1234567890";
        String Symbols = "&'*@{}[]^():,$=!><-%|+#;/?~._";
        String PasswordReq = "";
        boolean GeneratePassword = true;
        
        while (GeneratePassword == true){
            if (MaximumCB.getSelectedItem().equals("") || CapitalCB.getSelectedItem().equals("") || SymbolsCB.getSelectedItem().equals("") || NumbersCB.getSelectedItem().equals("") || LowerCB.getSelectedItem().equals("")){
                JOptionPane.showMessageDialog(rootPane, "There are boxes that are not filled in. Please fill them in.", "Missing Information Error", JOptionPane.INFORMATION_MESSAGE);
                GeneratePassword = false;
                break;
            }
            if (CapitalCB.getSelectedItem().equals("No") && SymbolsCB.getSelectedItem().equals("No") && NumbersCB.getSelectedItem().equals("No") && LowerCB.getSelectedItem().equals("No")){
                JOptionPane.showMessageDialog(rootPane, "Cannot create password with no characters. Please answer 'Yes' to at least 1 of the boxes.", "Password Generator Error", JOptionPane.INFORMATION_MESSAGE);
                GeneratePassword = false;   
                break;
            }
            if (CapitalCB.getSelectedItem().equals("Yes")){
                PasswordReq = PasswordReq + Capital;
            }
            if (SymbolsCB.getSelectedItem().equals("Yes")){
                PasswordReq = PasswordReq + Symbols;
            }
            if (NumbersCB.getSelectedItem().equals("Yes")){
                PasswordReq = PasswordReq + Numbers;
            }
            if (LowerCB.getSelectedItem().equals("Yes")){
                PasswordReq = PasswordReq + Lower;
            }
                
                int PasswordLength = Integer.parseInt((String)MaximumCB.getSelectedItem());
                for (int i = 0; i < PasswordLength; i++){
                    RandomLetter = PasswordReq.charAt(r.nextInt(PasswordReq.length()));
                    RandomPassword = RandomPassword + RandomLetter;
                }
                
                GenPassTB.setText(RandomPassword);
                break;
        
        
        }
        
        
        
    }//GEN-LAST:event_GenPassButtonActionPerformed

